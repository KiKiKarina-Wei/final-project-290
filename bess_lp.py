"""## 12 · Naive fixed-schedule baseline (lower bound)

"""

# Naive fixed schedule
CHARGE_HOURS = [10, 11, 12, 13]    # 10am-2pm
DISCHARGE_HOURS = [17, 18, 19, 20] # 5pm-9pm

# Energy-balanced rates: charge at 8 MW, discharge at 8 x eta = 6.8 MW
# (so what we charge in equals what we draw out, given round-trip loss)
C_RATE = 8.0                # MW charging
D_RATE = C_RATE * ETA_RT    # ~6.8 MW discharging

# Build hourly charge/discharge pattern
charge_pat = np.zeros(24)
discharge_pat = np.zeros(24)
for h in CHARGE_HOURS: charge_pat[h] = C_RATE
for h in DISCHARGE_HOURS: discharge_pat[h] = D_RATE

print(f"Naive schedule:")
print(f"  Charge   {CHARGE_HOURS} at {C_RATE} MW = {C_RATE * len(CHARGE_HOURS):.1f} MWh charged")
print(f"  Discharge {DISCHARGE_HOURS} at {D_RATE:.2f} MW = {D_RATE * len(DISCHARGE_HOURS):.1f} MWh delivered")

# Compute revenue per day under the fixed schedule
naive_records = []
for d_, g in m.groupby('date'):
    g = g.sort_values('timestamp_jst')
    if len(g) != 24: continue
    prices = g['price_tokyo'].values
    rev = float(np.sum(prices * (discharge_pat - charge_pat)) * 1000)
    naive_records.append({'date': d_, 'naive_rev': rev})

naive_df = pd.DataFrame(naive_records)
naive_df['date'] = pd.to_datetime(naive_df['date'])

naive_total = naive_df['naive_rev'].sum()
naive_neg_days = (naive_df['naive_rev'] < 0).sum()

print(f"\n=== Naive baseline results ===")
print(f"  Total over 3 years : ¥{naive_total:,.0f}  ({naive_total/1e6:.1f} M)")
print(f"  Mean per day       : ¥{naive_df['naive_rev'].mean():,.0f}")
print(f"  Days losing money  : {naive_neg_days} / {len(naive_df)}  ({naive_neg_days/len(naive_df)*100:.1f}%)")

