for i, d in enumerate(dates):
    g = daily.get_group(d).sort_values('timestamp_jst')
    prices = g['price_tokyo'].values
    r = solve_day(prices, return_plan=True)
    if r['status'] != 'Optimal':
        records.append({'date': d, 'revenue_jpy': np.nan})
        continue
    records.append({
        'date'        : d,
        'revenue_jpy' : r['revenue_jpy'],
        'max_price'   : float(prices.max()),
        'min_price'   : float(prices.min()),
        'mean_price'  : float(prices.mean()),
        'spread'      : float(prices.max() - prices.min()),
    })
    if (i + 1) % 200 == 0:
        print(f"  {i+1:>4d}/{len(dates)}  elapsed={time.time()-t0:.1f}s")

dt_total = time.time() - t0
print(f"\nSolved {len(records)} days in {dt_total:.1f}s ({dt_total/len(records)*1000:.1f} ms/day)")

