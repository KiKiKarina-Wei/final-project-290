"""## 8 · LP dispatch model

"""

# Battery parameters
P_MAX     = 10.0                  # MW
E_CAP     = 40.0                  # MWh (4-hour battery)
ETA_RT    = 0.85                  # round-trip efficiency
ETA_C     = np.sqrt(ETA_RT)       # ~0.9220 (charge leg)
ETA_D     = np.sqrt(ETA_RT)       # ~0.9220 (discharge leg)
SOC_MIN   = 0.10
SOC_MAX   = 0.90
SOC_FLOOR = SOC_MIN * E_CAP       # 4 MWh
SOC_CEIL  = SOC_MAX * E_CAP       # 36 MWh
USABLE    = SOC_CEIL - SOC_FLOOR  # 32 MWh
DT        = 1.0                   # hour
EPS       = 1e-4                  # tiny degeneracy penalty


def solve_day(prices_24, return_plan=True, verbose=False):
    """Solve one day's battery dispatch LP.

    Args:
        prices_24: iterable of 24 floats - hourly prices in JPY/kWh
        return_plan: if True, returns charge/discharge/SOC arrays
        verbose: solver messages on/off

    Returns:
        dict with: status, revenue_jpy, (optionally) charge, discharge, soc, prices
    """
    prices = np.asarray(prices_24, dtype=float)
    assert len(prices) == 24, f"Need 24 prices, got {len(prices)}"
    T = 24

    model = pulp.LpProblem("daily_arbitrage", pulp.LpMaximize)

    c = [pulp.LpVariable(f"c_{t}", lowBound=0, upBound=P_MAX) for t in range(T)]
    d = [pulp.LpVariable(f"d_{t}", lowBound=0, upBound=P_MAX) for t in range(T)]
    s = [pulp.LpVariable(f"s_{t}", lowBound=SOC_FLOOR, upBound=SOC_CEIL) for t in range(T)]

    # Objective in JPY: price [JPY/kWh] x MW x h x 1000 [kWh/MWh]
    revenue = pulp.lpSum(prices[t] * (d[t] - c[t]) * DT * 1000.0 for t in range(T))
    penalty = EPS * pulp.lpSum(c[t] + d[t] for t in range(T))
    model += revenue - penalty

    # SOC dynamics, starting empty (4 MWh)
    model += (s[0] == SOC_FLOOR + ETA_C * c[0] * DT - d[0] * DT / ETA_D), "soc_init"
    for t in range(1, T):
        model += (s[t] == s[t-1] + ETA_C * c[t] * DT - d[t] * DT / ETA_D), f"soc_{t}"

    # Diurnal closure
    model += (s[T-1] == SOC_FLOOR), "soc_closure"

    # One cycle per day (charge side)
    model += (pulp.lpSum(ETA_C * c[t] * DT for t in range(T)) <= USABLE), "one_cycle"

    solver = pulp.PULP_CBC_CMD(msg=1 if verbose else 0)
    status = model.solve(solver)

    if pulp.LpStatus[status] != 'Optimal':
        return {'status': pulp.LpStatus[status], 'revenue_jpy': np.nan}

    c_vals = np.array([c[t].varValue for t in range(T)])
    d_vals = np.array([d[t].varValue for t in range(T)])
    s_vals = np.array([s[t].varValue for t in range(T)])
    rev = float(np.sum(prices * (d_vals - c_vals) * DT) * 1000.0)

    out = {'status': 'Optimal', 'revenue_jpy': rev}
    if return_plan:
        out.update({'charge': c_vals, 'discharge': d_vals, 'soc': s_vals, 'prices': prices})
    return out

print("LP defined.")
print(f"  Power max        : {P_MAX} MW")
print(f"  Capacity         : {E_CAP} MWh")
print(f"  Round-trip eta   : {ETA_RT} (split as sqrt-eta on each leg = {ETA_C:.4f})")
print(f"  SOC bounds       : [{SOC_FLOOR}, {SOC_CEIL}] MWh")
print(f"  Usable per cycle : {USABLE} MWh")

